(defn name->entrypoint [name]
  (let [parts (str/split name #"_")
        ;; Take everything after the first 1-2 numeric parts
        func-parts (drop-while #(re-matches #"\d+" %) (rest parts))]
    (symbol (str/join "-" (map str/lower-case func-parts)))))

(defn humaneval-task [idx name]
  (let [entry (name->entrypoint name)]
    {:id         (format "humaneval-clj-%03d" (inc idx))
     :track      :function
     :source     :multipl-e/humaneval-clj
     :lang       :clojure
     :prompt-ref {:kind :multipl-e
                  :config "humaneval-clj"
                  :name name}
     :tests-ref  {:kind :multipl-e
                  :config "humaneval-clj"
                  :name name}
     :entrypoint entry
     :difficulty :unknown
     :tags       #{:general}
     :runner     {:kind :clojure-test
                  :timeout-sec 10
                  :memory-mb 1024}
     :license    {:kind :dataset
                  :name "MultiPL-E"
                  :spdx "MIT"
                  :redistributable true}
     :status     :active}))


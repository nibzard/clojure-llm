(defn -main []
  (let [tasks (concat
                (map-indexed humaneval-task humaneval-names)
                (map-indexed mbpp-task mbpp-names))
        path   "benchmark/tasks-v0.edn"]
    (spit path (with-out-str (pprint/pprint (vec tasks))))
    (println "Generated" (count tasks) "tasks to" path)
    (println "  humaneval-clj:" (count humaneval-names))
    (println "  mbpp-clj:" (count mbpp-names))))
